package com.syntax.class26;

public class Demo3 {
    public static void main(String[] args) {
        byte d = 10; // primitive dataType
        Byte d2 = 15; // non primitive dataType
        short s = 25;
        Short s2 = 25;
        char gender = 'M';
        Character gender2 = 'F';
        boolean flag = false;
        Boolean flag2 = true;
        int num = 10;
        Integer num2 = 20;
        float f = 5;  // for primitive java does it automatically
        Float f2 = 11f;
        double db = 10;// converts automatically for primitive
        Double db2 = 45.2; // they are classes not primitive classes anymore
        long l = 45;
        Long l2 = 45L; // Java does not upcast non-primitives data types automatically
    }
}
